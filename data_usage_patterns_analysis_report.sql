-- STEP 9: TRANSIENT TABLE CANDIDATES
-- ============================================================================
-- Tables with STG naming pattern are candidates for transient conversion.
-- Transient tables: 0-1 day Time Travel, NO Fail-safe (saves ~7 days fail-safe storage).
-- Note: All tables currently have IS_TRANSIENT = NO and FAILSAFE_BYTES = 0.

-- PROD_DATA.STG schema tables (20 tables) are candidates based on naming convention:
--   EBIKE_SIM_1 through EBIKE_SIM_20 in PROD_DATA.STG
--
-- Conversion SQL (example):
--   CREATE OR REPLACE TRANSIENT TABLE PROD_DATA.STG.EBIKE_SIM_1 AS SELECT * FROM PROD_DATA.STG.EBIKE_SIM_1;

SELECT 
    s.TABLE_CATALOG AS DATABASE_NAME,
    s.TABLE_SCHEMA AS SCHEMA_NAME,
    s.TABLE_NAME,
    s.IS_TRANSIENT AS CURRENT_IS_TRANSIENT,
    s.FAILSAFE_BYTES,
    CASE 
        WHEN s.TABLE_SCHEMA IN ('STG') THEN 'Staging/ETL naming pattern'
        ELSE 'No criteria met'
    END AS TRANSIENT_CRITERIA
FROM SNOWFLAKE.ACCOUNT_USAGE.TABLE_STORAGE_METRICS s
WHERE s.TABLE_CATALOG IN ('PROD_DATA','LDW','DELETED_TABLE')
  AND s.DELETED = FALSE
  AND s.TABLE_SCHEMA NOT IN ('INFORMATION_SCHEMA')
ORDER BY s.TABLE_CATALOG, s.TABLE_SCHEMA, s.TABLE_NAME;


